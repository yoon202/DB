set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "SCOTT"."SHOW_EMP" (eid IN NUMBER)
AS
    v_ename   emp.ename%TYPE;
    v_job     emp.job%TYPE;
    v_hiredate emp.hiredate%TYPE;
BEGIN
    SELECT ename, job, hiredate
    INTO v_ename, v_job, v_hiredate
    FROM emp
    WHERE empno = eid;

    DBMS_OUTPUT.PUT_LINE(
        '사번: ' || eid ||
        ', 이름: ' || v_ename ||
        ', 업무: ' || v_job ||
        ', 입사일: ' || TO_CHAR(v_hiredate, 'YYYY-MM-DD')
    );
END;

